void main(List<String> args) {
  
  //for(initialization;condition;incre/decre)
  for(int i=11;i<=10;i++){
    print('for loop i = $i');
  }

  //while(condition){body}
  int j =11;
  while(j<=10){
    print('while loop j = $j');
    j++;
  }
  //do while

  int k=11;
  do{
    print('do while k = $k');
    k++;
  }
  while(k<=10);
  //for each

}